//multiple parameter

public class multiplepara {
    static void myMethod(String fname, int age){
        System.out.println(fname + " is " + age);

    }
    public static void main(String args[]){
        myMethod("azhar", 20);
        myMethod("juned", 22);
        myMethod("vivek", 24);
        myMethod("ajbullah", 26);
    }
}
